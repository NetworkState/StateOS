template <typename STREAM>
static void WriteDefineStatement(STREAM&& defineString, USTRING prefix, USTRING name, TOKEN index, bool caseSensitive = false)
{
    LOCAL_STREAM<256> tempname;
    if (name == "*")
    {
        tempname.writeString("STAR");
    }
    else if (name == "/")
    {
        tempname.writeString("SLASH");
    }
    else
    {
        for (UINT32 i = 0; i < name.length(); i++)
        {
            if ((name[i] == '-') || (name[i] == ' ') || (name[i] == '!') || (name[i] == '/') || (name[i] == '+') || (name[i] == '.'))
            {
                tempname.writeByte('_');
            }
            else if (name[i] == '*')
            {
                tempname.writeString("STAR");
            }
            else if (name[i] == '/')
            {
                tempname.writeString("SLASH");
            }
            else
            {
                tempname.writeByte(caseSensitive ? name[i] : (UINT8)toupper(name[i]));
            }
        }
    }

    defineString.writeMany("constexpr auto ", prefix, "_", tempname.toBuffer(), " = TOKEN(0x");
    defineString.writeHex(index.toUInt32());
    //defineString.writeHex(index._type);
    //defineString.writeHex(index._value1);
    //defineString.writeHex(index._value2);
    //defineString.writeHex(index._value3);
    defineString.writeString("u);\r\n");
}

template <typename STREAM>
static void WriteDefineStatement(STREAM&& defineString, USTRING name, TOKEN index)
{
    char buffer[256];
    snprintf(buffer, 256, "constexpr auto %s = TOKEN(0x%08Xu);\r\n", name.data(), index.toUInt32());
    defineString.writeString(buffer);
}

constexpr USTRING DEFINE_FILENAME = "define.txt";
constexpr UINT32 DEFINE_BUFFER_SIZE = 128 * 1024 * 1024;
constexpr UINT32 ARRAY_BUFFER_SIZE = 2 * 1024 * 1024;

UINT8* defineBuffer;

void PopulateNames()
{
    BYTESTREAM defineString;
    defineBuffer = (UINT8*)MemAlloc(DEFINE_BUFFER_SIZE);

    defineString.setAddress(defineBuffer, DEFINE_BUFFER_SIZE);

    for (auto& prefixName : NameStrings)
    {
        TOKEN nameToken = CreateName(prefixName.name);
        WriteDefineStatement(defineString, prefixName.prefix, prefixName.name, nameToken, true);
    }

    for (auto& nameValue : NameValueStrings)
    {
        TOKEN nameToken = CreateName(nameValue.value);
        WriteDefineStatement(defineString, nameValue.name, nameToken);
    }

    File.WriteFile(DEFINE_FILENAME, defineString.toBuffer());

    MemFree(defineBuffer);
}
